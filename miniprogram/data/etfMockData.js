function toFund(row) {
  return {
    id: row[0],
    name: row[1],
    code: row[2],
    tags: row[3],
    nav: row[4],
    changePercent: row[5],
    trend: row[6],
    followed: row[7]
  };
}

function buildSectorEtfData(sector) {
  const overview = overviewRows[sector.id] || overviewRows.beauty;

  return {
    sectorId: sector.id,
    sectorName: sector.name,
    overview: {
      oneYearChange: overview[0],
      heat: overview[1],
      valuationPercentile: overview[2],
      keywords: overview[3]
    },
    funds: (fundRows[sector.id] || fundRows.beauty).map(toFund)
  };
}

